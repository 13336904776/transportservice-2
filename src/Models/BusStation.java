package Models;

public class BusStation {
	private int[] busId;
	private int[] Distance;
	public int[] getBusId() {
		return busId;
	}
	public void setBusId(int[] busId) {
		this.busId = busId;
	}
	public int[] getDistance() {
		return Distance;
	}
	public void setDistance(int[] distance) {
		Distance = distance;
	}
	
	
}
