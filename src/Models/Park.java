package Models;

public class Park {
	private String rateType;
	private int money;
	private int[] freeId;
	public String getRateType() {
		return rateType;
	}
	public void setRateType(String rateType) {
		this.rateType = rateType;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int[] getFreeId() {
		return freeId;
	}
	public void setFreeId(int[] freeId) {
		this.freeId = freeId;
	}
	
	
}
